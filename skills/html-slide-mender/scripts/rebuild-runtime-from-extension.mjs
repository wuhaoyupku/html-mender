#!/usr/bin/env node
import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const skillDir = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const repoRoot = resolve(skillDir, "../..");
const extensionDir = resolve(repoRoot, "extension");
const contentScriptListPath = resolve(extensionDir, "src/content/content-script-files.js");
const outputPath = resolve(skillDir, "assets/html-slide-mender-runtime.js");

const { CONTENT_SCRIPT_FILES } = await import(`${pathToFileURL(contentScriptListPath).href}?v=${Date.now()}`);

const parts = [
  banner(),
  skillChromePolyfill()
];

for (const file of CONTENT_SCRIPT_FILES) {
  const source = await readFile(resolve(extensionDir, file), "utf8");
  parts.push(`\n/* ===== ${file} ===== */\n${source}\n`);
}

parts.push(skillAutoStart());

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, parts.join("\n"), "utf8");
console.log(`Built ${outputPath} from ${CONTENT_SCRIPT_FILES.length} content-script files.`);

function banner() {
  return `/* HTML Slide Mender skill runtime.
 * Generated from extension/src/content via scripts/rebuild-runtime-from-extension.mjs.
 * Do not edit this file by hand; update extension runtime modules, then rebuild.
 */`;
}

function skillChromePolyfill() {
  return `
/* ===== skill chrome polyfill ===== */
(() => {
  const MESSAGE_NAMESPACE = "HTML_SLIDE_MENDER";
  const STORAGE_KEY = "__htmlSlideMenderSkillStorage";
  const listeners = [];

  function readStore() {
    try {
      return JSON.parse(window.localStorage.getItem(STORAGE_KEY) || "{}") || {};
    } catch (_error) {
      return {};
    }
  }

  function writeStore(store) {
    try {
      window.localStorage.setItem(STORAGE_KEY, JSON.stringify(store || {}));
    } catch (_error) {
      // localStorage can be disabled for some file:// contexts; in that case controls still work in memory.
    }
  }

  const storageLocal = {
    async get(keys) {
      const store = readStore();
      if (keys == null) {
        return { ...store };
      }
      if (Array.isArray(keys)) {
        return Object.fromEntries(keys.map((key) => [key, store[key]]));
      }
      if (typeof keys === "string") {
        return { [keys]: store[keys] };
      }
      if (typeof keys === "object") {
        return Object.fromEntries(Object.entries(keys).map(([key, fallback]) => [
          key,
          Object.prototype.hasOwnProperty.call(store, key) ? store[key] : fallback
        ]));
      }
      return {};
    },
    async set(values) {
      writeStore({ ...readStore(), ...(values || {}) });
    },
    async remove(keys) {
      const store = readStore();
      for (const key of Array.isArray(keys) ? keys : [keys]) {
        delete store[key];
      }
      writeStore(store);
    },
    async clear() {
      writeStore({});
    }
  };

  function downloadHtml(payload = {}) {
    const html = String(payload.html || "");
    const filename = String(payload.filename || "edited-html-slide.html");
    if (!html.trim()) {
      return { ok: false, error: "Nothing to download." };
    }

    const blob = new Blob([html], { type: "text/html;charset=utf-8" });
    const url = URL.createObjectURL(blob);
    const anchor = document.createElement("a");
    anchor.href = url;
    anchor.download = filename.toLowerCase().endsWith(".html") ? filename : filename + ".html";
    anchor.style.display = "none";
    document.documentElement.appendChild(anchor);
    anchor.click();
    anchor.remove();
    window.setTimeout(() => URL.revokeObjectURL(url), 1200);
    return { ok: true, downloadId: "skill-download" };
  }

  function sendToListeners(message) {
    return new Promise((resolve) => {
      let handledAsync = false;
      let settled = false;
      const sender = { tab: null };
      const sendResponse = (response) => {
        if (settled) {
          return;
        }
        settled = true;
        resolve(response);
      };

      for (const listener of [...listeners]) {
        const result = listener(message, sender, sendResponse);
        if (result === true) {
          handledAsync = true;
        } else if (result !== false && result !== undefined && !settled) {
          settled = true;
          resolve(result);
          return;
        }
      }

      if (!handledAsync && !settled) {
        resolve({ ok: false, error: "No editor listener handled the message." });
      }
      if (handledAsync) {
        window.setTimeout(() => {
          if (!settled) {
            settled = true;
            resolve({ ok: false, error: "Editor listener timed out." });
          }
        }, 30000);
      }
    });
  }

  window.chrome = window.chrome || {};
  window.chrome.storage = window.chrome.storage || {};
  window.chrome.storage.local = storageLocal;
  window.chrome.runtime = window.chrome.runtime || {};
  window.chrome.runtime.onMessage = {
    addListener(listener) {
      if (!listeners.includes(listener)) {
        listeners.push(listener);
      }
      return true;
    },
    removeListener(listener) {
      const index = listeners.indexOf(listener);
      if (index >= 0) {
        listeners.splice(index, 1);
      }
      return true;
    },
    hasListener(listener) {
      return listeners.includes(listener);
    }
  };
  window.chrome.runtime.sendMessage = async (message) => {
    if (message?.namespace === MESSAGE_NAMESPACE && message?.type === "DOWNLOAD_HTML") {
      return downloadHtml(message.payload);
    }
    return sendToListeners(message);
  };
})();
`;
}

function skillAutoStart() {
  return `
/* ===== skill autostart ===== */
(() => {
  const MESSAGE_NAMESPACE = "HTML_SLIDE_MENDER";

  function draftKey() {
    const options = window.__HTML_SLIDE_MENDER_SKILL_OPTIONS || {};
    return "htmlSlideMenderSkillDraft:" + (options.draftKey || location.href.split("#")[0]);
  }

  window.__HTML_SLIDE_MENDER_SKILL_SAVE_DRAFT__ = async (draft = {}) => {
    const savedAt = new Date().toISOString();
    try {
      localStorage.setItem(draftKey(), JSON.stringify({
        patches: Array.isArray(draft.patches) ? draft.patches : [],
        title: String(draft.title || document.title || ""),
        url: String(draft.url || location.href),
        savedAt,
        version: Number(draft.version || 2)
      }));
      return { ok: true, savedAt };
    } catch (error) {
      return {
        ok: false,
        error: "Draft is too large for browser local storage. Please download HTML instead."
      };
    }
  };

  window.__HTML_SLIDE_MENDER_SKILL_CLEAR_DRAFT__ = async () => {
    try {
      localStorage.removeItem(draftKey());
      return { ok: true };
    } catch (error) {
      return { ok: false, error: error?.message || String(error) };
    }
  };

  async function startSkillEditor() {
    const options = window.__HTML_SLIDE_MENDER_SKILL_OPTIONS || {};
    if (options.autoStart === false) {
      return;
    }

    await chrome.storage.local.set({
      htmlSlideMenderLanguage: options.lang === "en" ? "en" : "zh-CN",
      htmlSlideMenderExportMode: options.exportMode === "full" ? "full" : "basic"
    });

    const response = await chrome.runtime.sendMessage({
      namespace: MESSAGE_NAMESPACE,
      type: "EDITOR_COMMAND",
      command: "start",
      payload: {
        language: options.lang,
        exportMode: options.exportMode,
        mode: options.exportMode
      }
    });

    if (!response?.ok) {
      console.warn("[HTML Slide Mender Skill]", response?.error || "Editor did not start.");
    }
  }

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", () => {
      window.setTimeout(() => startSkillEditor().catch((error) => console.error("[HTML Slide Mender Skill]", error)), 0);
    }, { once: true });
  } else {
    window.setTimeout(() => startSkillEditor().catch((error) => console.error("[HTML Slide Mender Skill]", error)), 0);
  }
})();
`;
}
